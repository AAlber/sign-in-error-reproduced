export const getMeetingLinkType = (link: string) => {
  if (link.includes("teams.microsoft")) {
    return 1;
  } else if (link.includes("zoom.us")) {
    return 2;
  } else if (link.includes("webex.com")) {
    return 3;
  } else if (link.includes("meet.google.com")) {
    return 4;
  } else {
    return 0;
  }
};
